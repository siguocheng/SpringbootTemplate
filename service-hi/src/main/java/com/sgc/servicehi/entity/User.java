package com.sgc.servicehi.entity;

public class User {
    /**
     * 主键
     */
    private String userName;

    private Integer id;
    
	

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

    
}